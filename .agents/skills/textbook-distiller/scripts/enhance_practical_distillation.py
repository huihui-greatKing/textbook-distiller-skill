def write_study_guide(root: Path) -> None:
    lines = [
        "# 《计算机组成与设计》复习式蒸馏总览",
        "",
        "本文件是面向复习和作业的总览，不是模板清单。它把第 1 章到第 10 章整理成“学什么、怎么答题、哪里容易错”的形式。",
        "",
    ]
    for ch in CHAPTERS:
        lines.extend(
            [
                f"## 第{ch['num']}章 {ch['title']}",
                "",
                f"本章主线：{ch['main']}",
                "",
                "核心知识点：",
                *[f"- {name}：{desc}" for name, desc in ch["concepts"][:6]],
                "",
                "作业方法：",
                *[f"- {name}：{method}" for name, _, method in ch["problems"]],
                "",
                f"复习提醒：{ch['homework']}",
                "",
            ]
        )
    lines.extend(
        [
            "## 总体复习路径",
            "",
            "1. 先用第 1 章建立系统整体框架；",
            "2. 用第 2-3 章掌握数据表示和运算基础；",
            "3. 用第 4-6 章理解指令、CPU 和流水线；",
            "4. 用第 7-8 章理解存储和输入输出；",
            "5. 用第 9-10 章理解多机系统和 EDA 设计；",
            "6. 做作业时先判断章节，再调用对应章节 Skill。",
            "",
            f"生成时间：{datetime.now().isoformat(timespec='seconds')}",
        ]
    )
    (root / "knowledge-base/normalized/study-guide.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    root = Path(".")
    for sub in ["knowledge-base/normalized/concepts", "knowledge-base/normalized/problem-types"]:
        (root / sub).mkdir(parents=True, exist_ok=True)
    for ch in CHAPTERS:
        write_summary(root, ch)
        write_concepts(root, ch)
        write_problem_types(root, ch)
        write_skill(root, ch)
    write_study_guide(root)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
