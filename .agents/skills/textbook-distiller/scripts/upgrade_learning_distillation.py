def report() -> str:
    return """# 教材蒸馏课程实践报告

## 项目背景

本项目围绕《计算机组成与设计》教材展开。最开始的版本主要完成了 OCR、章节结构、章节概要和 Skill 文件生成，但实际使用后发现：只有概要总结并不能真正帮助我学会知识、复习重点、掌握题型和完成作业。

## 目标调整

项目目标从“教材概要总结”调整为“学习理解 + 复习整理 + 题型训练 + 作业书写辅助”。教材蒸馏不只是把一章压缩成几句话，而是要把教材中的概念、公式、图表、例题、题型和易错点整理成可以调用、可以练习、可以复用的 Skill。

## 为什么概要总结不够

概要总结只能告诉我本章大概讲了什么，但不能解决三个实际问题：第一，不知道概念为什么引入；第二，不知道题目从哪里入手；第三，写作业时仍然缺少完整过程。因此仅有章节简介和知识点列表是不合格的。

## 学习型蒸馏设计

学习型章节 Skill 按“学习目标、知识地图、概念精讲、公式规则、图表整理、典型题型、作业模板、复习提纲、自测题”组织。这样做的目的，是把教材中的知识转化为可学习、可复习、可做题的结构。

## 作业式解题模板

综合作业 Skill 被强化为完整作业答案生成流程。对简答题输出答题要点、分点作答、教材式表达和可写入作业的答案；对计算题输出已知条件、公式或方法、计算过程、结果和检查说明；对比较题使用表格，对综合题先拆题再作答。

## 测试验证

测试文件不再只检查是否能调用章节 Skill，而是检查是否讲得清楚、是否能用于复习、是否能生成完整作业答案、是否能解释为什么这样做、是否指出易错点、是否避免空泛总结。

## 项目成果

- 第 1 章到第 10 章学习型章节 Skill；
- 综合作业 Skill；
- 学习理解、复习提纲、作业解题和易错辨析测试；
- 面向学习和作业的评价标准；
- 迭代记录和待人工复核项。

## 不足与改进

由于教材来自扫描版 PDF，公式、表格、图示和例题题干仍需要人工复核。后续可以继续补充精确例题、图表截图、公式校对和真实作业题样例。

## 总结

本项目的重点不是让 AI 替代学习，而是把教材内容整理成更适合学生理解、复习和规范写作业的结构化 Skill。升级后的版本更接近我真正需要的教材蒸馏：既能学知识，也能练题和写作业。
"""


